
public class Snake {

    private int head;
    private int tail;

    public Snake(int h, int t) {
        head = h;
        tail = t;
    }

    public int getHead() {
        return head;
    }

    public int getTail() {
        return tail;
    }

    public void setHead(int head) {
        this.head = head;
    }

    public void setTail(int tail) {
        this.tail = tail;
    }


}
